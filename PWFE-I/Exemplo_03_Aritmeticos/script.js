// Operadores que realizam cálculos matemáticos
let a = 10;
let b = 5;

console.log("Valor de \"a\":", a);
console.log("Valor de \"b\":", b);

console.log("Soma: ", a + b);            //Soma: 15
console.log("Subtração: ", a - b);       //Subtração: 5
console.log("Multiplicação: ", a * b);   //Multipliação: 50
console.log("Divisão: ", a / b);         //Divisão: 2
console.log("Módulo: ", a % b);          //Módulo: 0
console.log("Exponenciação: ", a ** 2);  //Exponenciação: 100

