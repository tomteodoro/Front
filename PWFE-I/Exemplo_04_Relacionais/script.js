// **1.1.2. Relacionais**
// Comparativos que retornam verdadeiro ou falso

let a = 5;
let b = 10;

console.log("Valor de \"a\":", a);
console.log("Valor de \"b\":", b);

console.log("Maior que: ", a > b);              //true
console.log("Menor que: ", a < b);              //false
console.log("Igualdade: ", a == b);             //false
console.log("Estritamente igual", a === b);     //true
console.log("Diferente: ", a != b);             //true

